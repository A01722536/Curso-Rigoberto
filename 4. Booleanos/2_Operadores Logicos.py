#Operador AND
print (10>2 and 9>100)
print(10>2 and 23==23)

#Operador OR
print (10>2 or 9>100)
print(10>2 or 23==23)

#Operador NOT
print(not 90!=90)
print(not(10>2 or 23==23))

cadena="mimimimimi"
